    public static void DrawLines(SpriteBatch spriteBatch, Texture2D pixel, Vector2 startPosition, IEnumerable<string> lines, int scale, Color color)
    {
        var y = startPosition.Y;
        foreach (var line in lines)
        {
            DrawText(spriteBatch, pixel, line, new Vector2(startPosition.X, y), scale, color);
            y += 8 * scale;
        }
    }

    public static void DrawText(SpriteBatch spriteBatch, Texture2D pixel, string text, Vector2 position, int scale, Color color)
    {
        var x = position.X;

        foreach (var rawChar in text.ToUpperInvariant())
        {
            if (!Glyphs.TryGetValue(rawChar, out var glyph))
            {
                glyph = Glyphs[' '];
            }

            for (var row = 0; row < glyph.Length; row++)
            {
                for (var col = 0; col < glyph[row].Length; col++)
                {
                    if (glyph[row][col] == '1')
                    {
                        var rect = new Rectangle((int)x + (col * scale), (int)position.Y + (row * scale), scale, scale);
                        spriteBatch.Draw(pixel, rect, color);
                    }
                }
            }

            x += 6 * scale;
        }
    }
